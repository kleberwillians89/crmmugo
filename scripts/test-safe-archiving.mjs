import assert from 'node:assert/strict'
import {readFile} from 'node:fs/promises'
const migration=await readFile(new URL('../supabase/migrations/202607140002_sprint13_4_safe_archiving.sql',import.meta.url),'utf8')
for(const name of['archive_contract','restore_contract','get_contract_deletion_impact','delete_contract_permanently','archive_proposal','restore_proposal','get_proposal_deletion_impact','delete_proposal_permanently'])assert.match(migration,new RegExp(`function public\\.${name}`))
assert.match(migration,/coalesce\(received_amount,0\)>0/)
assert.match(migration,/status in\('paid','partial'\)/)
assert.match(migration,/Arquive o contrato antes da exclusão permanente/)
assert.match(migration,/vinculada a um contrato e não pode ser excluída permanentemente/)
assert.match(migration,/update public\.documents set contract_id=null/)
assert.match(migration,/update public\.documents set proposal_id=null/)
assert.doesNotMatch(migration,/drop table|truncate table|alter table .* disable row level security/i)
const contracts=await readFile(new URL('../src/services/data/contractsRepository.js',import.meta.url),'utf8')
for(const name of['archiveContract','restoreContract','getContractDeletionImpact','permanentlyDeleteContract'])assert.match(contracts,new RegExp(`function ${name}`))
assert.match(contracts,/resources:\['contracts','proposals','installments','dashboard','clients','intelligence'\]/)
const proposals=await readFile(new URL('../src/services/data/proposalsRepository.js',import.meta.url),'utf8')
for(const name of['archiveProposal','restoreProposal','getProposalDeletionImpact','permanentlyDeleteProposal'])assert.match(proposals,new RegExp(`function ${name}`))
const modal=await readFile(new URL('../src/components/PermanentDeletionModal.jsx',import.meta.url),'utf8')
assert.match(modal,/confirmation!=='EXCLUIR'/)
assert.match(modal,/impact\?\.blocked/)
const page=await readFile(new URL('../src/components/SupabaseContractsPage.jsx',import.meta.url),'utf8')
assert.match(page,/\['active','Ativos'\],\['cancelled','Cancelados'\],\['archived','Arquivados'\]/)
assert.match(page,/disabled=\{saving\}/)
console.log('Safe archiving tests passed: permissions, impact, payment blocks, preservation, filters and confirmation.')
